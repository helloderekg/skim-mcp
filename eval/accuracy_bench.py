"""Accuracy-vs-cost yardstick - the number the category doesn't publish.

Every context-compression tool advertises a reduction percentage. None of them publish whether
the AGENT STILL GETS THE ANSWER RIGHT at that cost. This harness measures both halves for skim,
and defines a protocol any other tool can be scored under:

  COST (this script, deterministic, no model):
      for each premise-locked question in QUESTIONS.md, the exact tokens a session pays
      under (a) a full read and (b) skim, where the skim cost = skeleton + the SPAN THAT
      CONTAINS THE ANSWER. That span is derived from the fixture by content, and the premise
      "the answer is hidden behind exactly that anchor, never in the skeleton" is locked by
      tests/test_eval_premise.py. This is the ORACLE cost: what skim costs when the model
      expands exactly what it needs. Whether it does is the second half:

  CORRECTNESS (live protocol, real sessions):
      mount skim, ask the same questions in a fresh session with no hints, then score the
      call log with eval/score_expand_loop.py (expand-when-needed rate, pass bar >= 80%).

Scoring another tool: report (1) tokens shown to the model to answer each question, and
(2) whether the answer-bearing lines were available VERBATIM. Lossy maps score (2) = no.

    uv run python eval/accuracy_bench.py       # regenerates eval/ACCURACY.md and prints it
"""
import os
import sys

sys.path.insert(0, os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "src"))
from skim import count_tokens, skim_file, token_basis  # noqa: E402

_EVAL = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, _EVAL)
from score_expand_loop import KEY  # noqa: E402  - ONE marker table for the scorer and this pricer

TASKS = [(q, fname, marker) for q, (fname, marker) in KEY.items()]


def rows():
    out = []
    for qid, fname, marker in TASKS:
        r = skim_file(os.path.join(_EVAL, fname))
        full = count_tokens(r.full_text)
        skel = count_tokens(r.skeleton)
        aids = sorted(a for a in r.anchors if marker in r.expand(a))
        spans = sum(count_tokens(r.expand(a)) for a in aids)
        out.append({"q": qid, "file": fname, "anchors": aids, "full": full,
                    "skim": skel + spans, "skel": skel, "spans": spans})
    return out


def main():
    data = rows()
    tf = sum(d["full"] for d in data)
    ts = sum(d["skim"] for d in data)
    lines = [
        "# Accuracy vs cost - the yardstick",
        "",
        f"Generated by `uv run python eval/accuracy_bench.py` (token basis: {token_basis()}).",
        "Do not edit by hand.",
        "",
        "**What this is.** Reduction percentages are easy; the honest question is *does the agent*",
        "*still answer correctly, and what does the answer actually cost?* This table prices the",
        "seven premise-locked questions from [QUESTIONS.md](QUESTIONS.md) two ways: a full read,",
        "and skim's **oracle cost** (skeleton + exactly the answer-bearing span, derived from the",
        "fixture by content). The premise that every answer hides behind an anchor - never in the",
        "skeleton - is locked by `tests/test_eval_premise.py`, so the oracle span is well-defined.",
        "",
        "| question | file | full read | skim oracle (skeleton + span) | saved |",
        "|---|---|---:|---:|---:|",
    ]
    for d in data:
        pct = round(100 * (1 - d["skim"] / max(1, d["full"])))
        lines.append(f"| {d['q']} | `{d['file']}` | {d['full']:,} | "
                     f"{d['skim']:,} ({d['skel']:,}+{d['spans']:,}) | **{pct}%** |")
    tot = round(100 * (1 - ts / max(1, tf)))
    lines += [
        f"| **total** | | **{tf:,}** | **{ts:,}** | **{tot}%** |",
        "",
        "These fixtures are deliberately SMALL (a 46-line module, a short log) - near skim's",
        "worst case, since the skeleton wrapper is a fixed cost. On large files the same",
        "mechanism measures 30-91% per task (median 51% across 24 stdlib modules; see",
        "[BENCHMARKS.md](../BENCHMARKS.md)). Costs exclude the JSON tool-result envelope,",
        "which both approaches pay.",
        "",
        "## The correctness half (live protocol)",
        "",
        "Oracle cost assumes the model expands exactly what it needs. Whether it DOES is the",
        "under-fetch question, and it is measured, not assumed:",
        "",
        "1. Mount skim and open a **fresh** session (no hints beyond 'use skim').",
        "2. Ask the seven questions in [QUESTIONS.md](QUESTIONS.md) against the fixtures.",
        "3. `uv run python eval/score_expand_loop.py` scores the call log mechanically:",
        "   expand-when-needed rate (pass bar >= 80%), confident-wrong-without-expand (< 10%),",
        "   over-expansion (watch).",
        "",
        "## Scoring another tool on the same yardstick",
        "",
        "Report, per question: **(1)** total tokens shown to the model to reach the answer, and",
        "**(2)** whether the answer-bearing lines were available to the model *verbatim* at answer",
        "time (a lossy repo-map or paraphrasing summarizer scores no on 2 - if the surviving text",
        "happens to contain the answer, say so, but paraphrase is not verbatim). PRs adding runner",
        "adapters for other tools are welcome; the fixtures and markers are in this file's `TASKS`.",
    ]
    md = "\n".join(lines) + "\n"
    out_path = os.path.join(_EVAL, "ACCURACY.md")
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(md)
    print(md)
    print(f"wrote {out_path}")


if __name__ == "__main__":
    main()
